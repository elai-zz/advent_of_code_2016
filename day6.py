	column7 = [0 for _ in range(26)]

	columns = [column0, column1, column2, column3, column4, column5, column6, column7]
	answer = []

	def parse_line(self, line):
		for i in range(8):
			self.put_in_column(self.columns[i], line[i])

	def put_in_column(self, column, letter):
		index = ord(letter) - 97
		column[index] += 1

	def find_max_per_column(self, column):
		index = -1
		max_so_far = 0
		for i in range(len(column)):
			if column[i] > max_so_far:
				index = i
				max_so_far = column[i]
		return chr(index + 97)

	def find_min_per_column(self, column):
		index = -1
		min_so_far = 100
		for i in range(len(column)):
			if column[i] < min_so_far:
				index = i
				min_so_far = column[i]
		return chr(index + 97)

	def find_answer(self):
		for column in self.columns:
			#self.answer.append(self.find_max_per_column(column))
			self.answer.append(self.find_min_per_column(column))
		return ("").join(self.answer)


